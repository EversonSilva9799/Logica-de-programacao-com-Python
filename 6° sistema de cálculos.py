#Sistema de cálculos

x = int(input("Digite o primeiro valor: "))
y = int(input("Digite o segundo valor: "))

print("A soma de",x,"+",y,"é igual a",x + y)
print("A subtração",x,"-",y,"é igual a",x - y)
print("A multiplicação",x,"x",y,"é igual a",x * y)
print("A divisão de",x,":",y,"é igual a",x / y)
