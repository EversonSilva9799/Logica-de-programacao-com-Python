#Estrutura condicional if,else,elif

semana = int(input("Digite um número qualquer: "))

if semana == 1 :
     print("Domingo")
elif semana == 2 :
     print("Segunda")
elif semana == 3 :
     print("Terça")
elif semana == 4 :
     print("Quarta")
elif semana == 5 :
     print("Quinta")
elif semana == 6 :
     print("Sexta")
elif semana == 7 :
     print("Sábado")
else:
    print("Não corresponde a nenhum dia da semana")
