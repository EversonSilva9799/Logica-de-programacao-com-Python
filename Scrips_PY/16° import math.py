import math


#Para importa apenas um item de uma biblioteca,por exemplo sqrt faz assim:

#from math import sqrt

resp = math.sqrt(9) #Raiz quadrada
print(resp)

resp = math.factorial(5) #Calcula o fatorial
print(resp)

resp = math.ceil(5.5) #Arredonda para cima
print(resp)

resp = math.floor(5.5) #Arredonda para baixo
print(resp)

resp = math.isfinite(5.5) # Verifica se é infinito
print(resp)


resp = math.log(10) #Logaritmo natual
print(resp)

resp = math.log10(10) #Logaritmo na base 10
print(resp)

resp = math.log2(2) #Logaritmo na base 2
print(resp)

resp = math.pow(2, 3) #Potência 2^8
print(resp)



