#Comparadores e condicionais

'''
Algoritmo que verifica se pode dirigir ou não.
'''




idade = int(input("Digite sua idade: "))

if idade >= 18:
    print("Você já pode dirigir!") # Aqui é necessário identação
else:
    print("Você não pode dirigir!") # Aqui também


# Estrutura condicional "if" significa "se"
# Estrutura condicional "else" significa "senão"
# Estrutura condicional "elif" significa "senão se"



             
